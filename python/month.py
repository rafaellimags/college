month = input('Mês do ano(número): ')

def check_month(month):
    if (month == '1'):
        print('Janeiro')
    if (month == '2'):
        print('Fevereiro')
    if (month == '3'):
        print('Março')
    if (month == '4'):
        print('Abril')
    if (month == '5'):
        print('Maio')
    if (month == '6'):
        print('Junho')
    if (month == '7'):
        print('Julho')
    if (month == '8'):
        print('Agosto')
    if (month == '9'):
        print('Setembro')
    if (month == '10'):
        print('Outubro')
    if (month == '11'):
        print('Novembro')
    if (month == '12'):
        print('Dezembro')

check_month(month)